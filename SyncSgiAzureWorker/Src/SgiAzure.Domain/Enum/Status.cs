namespace SgiAzure.Domain.Enum
{
    /// <summary>
    /// Enum que representa los posibles estados de habilitación de un objeto o entidad.
    /// </summary>
    public enum Status
    {
        /// <summary>
        /// El objeto o entidad está habilitado y puede ser utilizado o procesado.
        /// </summary>
        Enabled,

        /// <summary>
        /// El objeto o entidad está deshabilitado y no puede ser utilizado o procesado.
        /// </summary>
        Disabled
    }
}
